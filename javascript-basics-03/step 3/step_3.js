
function changeColor(color){
    switch (color){
        case 'green' :
            document.getElementById('text').style.color = 'green';
            break;
    
    case 'red':
        document.getElementById('text').style.color = 'red';
            break;
            case 'blue':
                document.getElementById('text').style.color = 'blue';
            break;
            default:
       }   

}